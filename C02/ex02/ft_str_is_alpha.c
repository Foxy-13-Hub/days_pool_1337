int	ft_str_is_alpha(char *str)
{
	int	d;

	d = 0;
	if (str[d] != '\0')
	{
		while ((str[d] <= 122 && str[d] >= 97)
			|| (str[d] <= 90 && str[d] >= 65))
		{
			d++;
			if (str[d] == '\0')
				return (1);
		}
		return (0);
	}
	return (1);
}
