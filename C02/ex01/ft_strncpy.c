char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	r;

	r = 0;
	while (src[r] != '\0' && n > r)
	{
		dest[r] = src[r];
		r++;
	}
	while (n > r)
	{
		dest[r] = '\0';
		r++;
	}
	return (dest);
}
