int	ft_str_is_printable(char *str)
{
	int	f;

	f = 0;
	if (str[f] != '\0')
	{
		while (str[f] <= 126 && str[f] >= 32)
		{
			f++;
			if (str[f] != '\0')
				return (1);
		}
		return (0);
	}
	return (1);
}
