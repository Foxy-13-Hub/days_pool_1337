#include <unistd.h>

void	ft_write(char f)
{
	write(1, &f, 1);
}

void	ft_putstr(char *str)
{
	int	e;

	e = 0;
	while (str[e] != '\0')
	{
		ft_write(str[e]);
		e++;
	}
}
