int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	f;

	f = 0;
	if (n <= 0)
		return (0);
	while (s1[f] != '\0' && s2[f] != '\0' && n - 1 > f)
	{
		if (s1[f] != s2[f])
			return (s1[f] - s2[f]);
		f++;
	}
	return (s1[f] - s2[f]);
}
