char	*ft_strstr(char *str, char *to_find)
{
	int	f;
	int	r;

	f = 0;
	if (to_find[f] == '\0')
		return (str);
	while (str[f] != '\0')
	{
		r = 0;
		while (to_find[r] == str[f + r])
		{
			r++;
			if (to_find[r] == '\0')
				return (&str[f]);
		}
		f++;
	}
	return (NULL);
}
